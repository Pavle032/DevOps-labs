def find_max_subarray(arr):
  
    if not arr:
        return 0, []
    
    # Инициализация состояний
    max_ending_here = arr[0]  # dp[i] в текущий момент
    max_so_far = arr[0]       # глобальный максимум
    
    # Для восстановления подмассива
    start = 0
    end = 0
    temp_start = 0
    
    # Проходим по массиву с 1-го индекса
    for i in range(1, len(arr)):
        # Ключевой переход ДП: либо начинаем новый подмассив,
        # либо продолжаем существующий
        if arr[i] > max_ending_here + arr[i]:
            # Начинаем новый подмассив
            max_ending_here = arr[i]
            temp_start = i
        else:
            # Продолжаем существующий
            max_ending_here = max_ending_here + arr[i]
        
        # Обновляем глобальный максимум
        if max_ending_here > max_so_far:
            max_so_far = max_ending_here
            start = temp_start
            end = i
    
    # Извлекаем найденный подмассив
    result_subarray = arr[start:end + 1]
    
    return max_so_far, result_subarray



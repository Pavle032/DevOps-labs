def solve_three_knapsacks(items, capacities):
    """
    Решение задачи о трех рюкзаках методом ДП.
    Состояние: dp[i][w1][w2][w3] - максимальная стоимость 
    для первых i предметов и весов рюкзаков w1,w2,w3
    """
    n = len(items)
    c1, c2, c3 = capacities
    
    # Создаем 4D массив для ДП
    # dp[w1][w2][w3] - максимальная стоимость при текущих весах
    dp = [[[0] * (c3 + 1) for _ in range(c2 + 1)] for _ in range(c1 + 1)]
    
    # Для восстановления ответа храним, какой предмет куда положили
    # choice[w1][w2][w3] = (предмет_положили_в_1, предмет_положили_в_2, предмет_положили_в_3)
    choice = [[[None for _ in range(c3 + 1)] for _ in range(c2 + 1)] for _ in range(c1 + 1)]
    
    # Перебираем все предметы
    for idx, (name, weight, value) in enumerate(items):
        # Идем с конца, чтобы не использовать предмет несколько раз
        for w1 in range(c1, -1, -1):
            for w2 in range(c2, -1, -1):
                for w3 in range(c3, -1, -1):
                    current_val = dp[w1][w2][w3]
                    
                    # Вариант 1: не брать предмет
                    best_val = current_val
                    best_choice = None
                    
                    # Вариант 2: положить в рюкзак 1
                    if w1 >= weight and dp[w1 - weight][w2][w3] + value > best_val:
                        best_val = dp[w1 - weight][w2][w3] + value
                        best_choice = (1, idx)
                    
                    # Вариант 3: положить в рюкзак 2
                    if w2 >= weight and dp[w1][w2 - weight][w3] + value > best_val:
                        best_val = dp[w1][w2 - weight][w3] + value
                        best_choice = (2, idx)
                    
                    # Вариант 4: положить в рюкзак 3
                    if w3 >= weight and dp[w1][w2][w3 - weight] + value > best_val:
                        best_val = dp[w1][w2][w3 - weight] + value
                        best_choice = (3, idx)
                    
                    # Сохраняем лучшее значение и выбор
                    dp[w1][w2][w3] = best_val
                    if best_choice is not None:
                        choice[w1][w2][w3] = best_choice
    
    # Максимальная стоимость
    max_value = dp[c1][c2][c3]
    
    # Восстанавливаем, какие предметы в какие рюкзаки попали
    bag1 = []
    bag2 = []
    bag3 = []
    
    w1, w2, w3 = c1, c2, c3
    used = [False] * n
    
    # Идем назад по выбору
    while w1 > 0 or w2 > 0 or w3 > 0:
        if choice[w1][w2][w3] is None:
            break
        bag_num, item_idx = choice[w1][w2][w3]
        used[item_idx] = True
        
        name, weight, value = items[item_idx]
        
        if bag_num == 1:
            bag1.append((name, weight, value))
            w1 -= weight
        elif bag_num == 2:
            bag2.append((name, weight, value))
            w2 -= weight
        else:
            bag3.append((name, weight, value))
            w3 -= weight
    
    return max_value, bag1, bag2, bag3


def print_bag(name, bag):
    """Выводит содержимое рюкзака"""
    print(f"\n{name} (вес/стоимость):")
    if not bag:
        print("  Пусто")
        return
    
    total_weight = 0
    total_value = 0
    
    for item, weight, value in bag:
        print(f"  - {item}: вес {weight}, стоимость {value}")
        total_weight += weight
        total_value += value
    
    print(f"  Итого: вес {total_weight}, стоимость {total_value}")


